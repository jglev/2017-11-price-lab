"""Demonstrate different types of variables."""

# An "integer" (a whole number)
test1 = 123
round(test1)

# Text
test2 = "123"
round(test2)  # This won't work -- you can't round text!

# A "floating-point" number (that is, a number with a decimal)
test3 = 123.2
type(round(test3))

# Lists
type(range(0, 13))
